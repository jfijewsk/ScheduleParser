

public class Technician {

	String name;
	String branch;
	String startDate;
	
	public Technician(String name, String branch, String startDate) {
		this.name = name;
		this.branch = branch;
		this.startDate = startDate;
	}

	public String getName() {
		return name;
	}

	public String getBranch() {
		return branch;
	}

	public String getStartDate() {
		return startDate;
	}
	
	
	
}
